#include <unistd.h>

void ft_print_comb2(void) {
    int i;
    int j;
    char record[5];

    i = 0;
    while (i <= 98) {
        j = i + 1;
        while (j <= 99) {
            record[0] = i / 10 + 48; //98 / 10 = 9
            record[1] = i % 10 + 48; // 98 % 10 = 8
            record[2] = ' ';
            record[3] = j / 10 + 48;// 99 / 10 = 9
            record[4] = j % 10 + 48;// 99 % 10 = 9
            write(1, record, 5);// record = "98 99"
            if ((record[0] != 9 + 48) || (record[1] != 8 + 48)) {
                write(1, ", ", 2);
            }
            j++;// 123 / 10
        }
        i++;
    }
}